void main() {
  var number = 6;

  if (number % 2 == 0) {
    print("it's even");
  }
  if (number % 5 == 0) {
    print("it is divisible by 5");
  } else {
    print("it is not divisible by 5");
  }

  if (number % 2 == 1) {
    print("it's odd");
  }
  if (number % 7 == 0) {
    print("it is divisible by 7");
  } else {
    print("it is not divisible by 7");
  }
}
